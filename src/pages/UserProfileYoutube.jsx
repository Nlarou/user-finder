function UserProfileYoutube() {
  const { channel, loading, videos, dispatch } = useContext(youtubeContext);
  const params = useParams();

  useEffect(() => {
    dispatch({ type: "SET_LOADING" });
    const getUserData = async () => {
      const userData = await getChannelAndVideos(params.login);
      console.log(userData);
      dispatch({ type: "GET_CHANNEL_AND_VIDEOS", payload: userData });
    };
    getUserData();
  }, []);

  const {
    snippet = {
      title: "",
      description: "",
      publishedAt: "",
      thumbnails: { medium: { url: "" } },
    },
    statistics = {
      viewCount: "",
      subscriberCount: "",
      hiddenSubscriberCount: "",
      videoCount: "",
    },
  } = channel;

  const channel_url = `https://www.youtube.com/channel/${channel.id}`;

  //Find the user country with the two letter country code
  const findCountry = (country_code) => {
    const country_name = countries.map((country) => {
      if (country.code === country_code) {
        return country.name;
      }
    });
    return country_name;
  };

  if (loading) {
    return <Spinner />;
  }
  return (
    <>
      <div className="w-full mx-auto lg:w-10/12">
        <div className="mb-4">
          <Link to="/" className="btn btn-ghost btn-lg">
            Back to search
          </Link>
        </div>
        <div className="grid grid-cols-1 xl:grid-cols-3 lg:grid-cols-3 mb-8 md:gap-8">
          <div className="custom-card-image mb-6 md:mb-0 rounded-full">
            <div className="shadow-xl card image-full rounded-full w-fit">
              <figure>
                {channel && (
                  <img
                    alt={snippet.title}
                    src={snippet.thumbnails.medium.url}
                    className="rounded-full"
                  />
                )}
              </figure>
              <div className="card-body justify-end mx-auto">
                <h2 className="card-title mb-1">{snippet.title}</h2>
                <p>{snippet.customUrl}</p>
              </div>
            </div>
          </div>
          <div className="col-span-2">
            <div className="mb-6">
              <h1 className="text-3xl card-title">
                {snippet.title}
                <div className="ml-1 mr-1 badge badge-success">
                  Created: {snippet.publishedAt}
                </div>
              </h1>
              <p>{snippet.description}</p>
              <div className="mt-4 card-actions">
                <a
                  href={channel_url}
                  target="_blank"
                  rel="noreferrer"
                  className="btn btn-ghost btn-sm"
                >
                  View on Youtube
                </a>
              </div>
            </div>

            <div className="w-full rounded-lg shadow-md bg-base-100 stats">
              {snippet.country && (
                <div className="stat">
                  <div className="stat-title text-md">
                    Country
                    <div className="text-lg stat-value ">
                      {findCountry(snippet.country)}
                    </div>
                  </div>
                </div>
              )}
            </div>
          </div>
        </div>
        <div className="w-full py-5 mb-6 rounded-lg shadow-md bg-base-100 stats">
          <div className="stat">
            <div className="stat-figure text-secondary">
              <FaEye className="text-3xl md:text-5xl" />
            </div>
            <div className="stat-title pr-5">Total Views</div>
            <div className="stat-value pr-5 text-3xl md:4xl">
              {statistics.viewCount}
            </div>
          </div>
          <div className="stat">
            <div className="stat-figure text-secondary">
              <FaUsers className="text-3xl md:text-5xl" />
            </div>
            <div className="stat-title pr-5">Subscribers</div>
            <div className="stat-value pr-5 text-3xl md:4xl">
              {statistics.subscriberCount}
            </div>
          </div>
          <div className="stat">
            <div className="stat-figure text-secondary">
              <FaPhotoVideo className="text-3xl md:text-5xl" />
            </div>
            <div className="stat-title pr-5">Videos</div>
            <div className="stat-value pr-5 text-3xl md:4xl">
              {statistics.videoCount}
            </div>
          </div>
        </div>
        <VideosList videos={videos} />
      </div>
    </>
  );
}

export default UserProfileYoutube;
